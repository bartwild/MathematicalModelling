import numpy as np
import matplotlib.pyplot as plt
alphas = np.append(np.arange(0, 1.1, 0.1), [2.0, 3.0])
income_ys = []
emissions_ys = []
cost_ys = []
for alpha in alphas:
    f1 = 150 - 20 * (1-alpha)
    f2 = 30 + 5 * (1-alpha)
    f3 = 70 + 10 * (1-alpha)
    income_ys.append(f1)
    emissions_ys.append(f2)
    cost_ys.append(f3)
income_xs = list(reversed([190, 170, 150, 148, 146, 144, 142, 140, 138, 136, 134, 132, 130]))
emissions_xs = list(reversed([24.2105, 23.1579, 22.1053, 22, 21.8947, 21.7895, 21.6842, 21.5789, 21.4737, 21.3684, 21.2632, 21.1579, 21.0526]))
cost_xs = list(reversed([36.6316, 33.4737, 30.3158, 30, 29.6842, 29.3684, 29.0526, 28.7368, 28.4211, 28.1053, 27.7895, 27.4737, 27.1579]))
plt.figure(figsize=(8, 12))
plt.subplot(3, 1, 1)
plt.title("Zysk")
plt.plot(income_ys, alphas, label="µ_G(x)-funkcja przynależności celu")
plt.plot(income_xs, alphas, label="µ_f(x)-wartości rozwiązania")
plt.xlabel('Wartość zysku C')
plt.ylabel('alfa')
plt.grid(True)
plt.legend()
plt.subplot(3, 1, 2)
plt.title("Emisja")
plt.plot(emissions_ys, alphas, label="µ_G(x)-funkcja przynależności ograniczenia")
plt.plot(emissions_xs, alphas, label="µ_f(x)-wartości rozwiązania")
plt.xlabel('Wartość emisji Z')
plt.ylabel('alfa')
plt.grid(True)
plt.legend()
plt.subplot(3, 1, 3)
plt.title("Koszt")
plt.plot(cost_ys, alphas, label="µ_G(x)-funkcja przynależności ograniczenia")
plt.plot(cost_xs, alphas, label="µ_f(x)-wartości rozwiązania")
plt.xlabel('Wartość kosztu K')
plt.ylabel('alfa')
plt.grid(True)
plt.legend()
plt.tight_layout()
plt.show()
